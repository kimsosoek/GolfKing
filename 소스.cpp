//
//클래스 설계
//
//1. 게임매니저(플레이어, 홀 관리)
//2. 플레이어
//3. 홀
//
//4. 아이템(골프 클럽, 신발(파생클래스)
//    5. 인벤토리(플레이어가 가지고 있음, 아이템 가지고 있음)
//
//1. 조작 방식(일반 샷과 동일)
//
//플레이어는 일반 샷처럼 방향을 조준하고, 문자열 길이로 파워를 입력합니다.
//
//단, 퍼팅에서는 바람(wind)의 영향이 없습니다.
//
//2. 성공 판정 로직(2단계)
//퍼팅의 성공 여부는 두 단계에 걸쳐서 판정됩니다.
//
//1단계: '스위트 스팟' 판정(완벽한 성공)
//* *'퍼팅에서의 스위트 스팟' * *이란, 홀까지 남은 거리에 정확히 필요한 만큼의 파워를 입력하는 것을 의미합니다. (예 : 8m 남았을 때, 12글자의 입력이 8m를 보내는 완벽한 파워라면, 12글자가 바로 스위트 스팟)
//
//만약 플레이어가 이 스위트 스팟에 완벽하게 맞춰서 파워를 입력했다면, greendifficulty와 상관없이 100 % 확률로 홀인합니다.이것은 플레이어의 순수한 컨트롤 실력에 대한 보상입니다.
//
//게임 메시지 : "완벽한 퍼팅! 그대로 홀컵으로 빨려 들어갑니다!"
//
//2단계 : '성공률' 판정(확률적 성공)
//만약 플레이어의 파워 입력이 스위트 스팟은 아니었지만, 어느 정도 근접했다면(컨트롤 구간 안이라면) 이 확률 판정을 시작합니다.
//
//성공률 계산 : greendifficulty가 높을수록 성공 확률이 낮아집니다.
//
//성공률(%) = 100 - (greendifficulty * 15)
//
//예시 :
//
//	greendifficulty 1 (쉬움) : 성공률 85 %
//
//	greendifficulty 3 (보통) : 성공률 55 %
//
//	greendifficulty 5 (어려움) : 성공률 25 %
//
//	주사위 굴리기 : gamemanager가 1~100 사이의 주사위를 굴립니다.
//
//	판정 :
//
//	주사위 ≤ 성공률 : 성공!홀인합니다.
//
//	게임 메시지 : "운이 좋았네요! 공이 컵에 쏙 들어갑니다!"
//
//	주사위 > 성공률: 실패.홀을 아깝게 비껴가고, 짧은 거리를 남긴 채 퍼팅을 다시 해야 합니다.
//
//	게임 메시지 : "이런! 그린의 경사를 잘못 읽었나 봅니다. 공이 홀컵을 비껴갑니다."